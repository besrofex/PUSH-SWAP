#include "../push_swap.h"

int error_sytax(char *str)
{
    int i;

    i = 0;
    while (str[i])
    {
        if (str[i] == '+' || str[i] == '-')
            return (1);
        if (str[i] <= '0' || str[i] >= '9')
            return (1);
        i++;
    }
    return (0);
}